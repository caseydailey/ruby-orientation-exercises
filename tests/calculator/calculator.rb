class Calculator

    # contains methods to perform four operations:
    
    # add(a,b)
    #  -args: a = int, b= int
    #  -returns: int: sum of a and b
    def add(a,b) 
        a + b 
    end

    # subtract(a,b)
    #  -args: a = int, b= int
    #  -returns: int: difference of a and b
    def subtract(a,b)
        a - b
    end

    # multiply(a,b)
    # -args: a = int, b= int
    #  -returns: int: product of a and b
    def multiply(a,b)
        a * b 
    end

    # divide(a,b)
    #  -args: a = int, b= int
    #  -returns: int: quotient of a and b
    def divide(a,b)
        a / b
    end

end
